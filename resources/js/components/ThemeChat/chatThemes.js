export const DEFAULT_CHAT_THEME = 'default';

const toKebabCase = (str) => str.replace(/([A-Z])/g, '-$1').toLowerCase();

export const applyChatTheme = (themeName) => {
    const theme = chatThemes[themeName];
    if (!theme) return;
    const root = document.documentElement;
    Object.entries(theme.colors).forEach(([key, value]) => {
        root.style.setProperty(key, value);
    });
};

export const getStoredChatTheme = (chatId) => {
    if (typeof window === 'undefined' || !chatId) return DEFAULT_CHAT_THEME;
    const saved = localStorage.getItem(`chatTheme_${chatId}`);
    return chatThemes[saved] ? saved : DEFAULT_CHAT_THEME;
};

export const persistChatTheme = (chatId, themeName) => {
    if (typeof window === 'undefined' || !chatId) return;
    localStorage.setItem(`chatTheme_${chatId}`, themeName);
    applyChatTheme(themeName);
};

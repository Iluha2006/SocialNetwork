const getStoredAppTheme = (userId) => {
  if (typeof window === 'undefined' || !userId) return DEFAULT_APP_THEME;
  const saved = localStorage.getItem(`appTheme_${userId}`);
  return appThemes[saved] ? saved : DEFAULT_APP_THEME;
};

const getStoredTextGradient = (userId) => {
  if (typeof window === 'undefined' || !userId) return 'none';
  const saved = localStorage.getItem(`textGradient_${userId}`);
  return textGradients[saved] ? saved : 'none';
};

export const persistAppTheme = (userId, themeName) => {
  if (typeof window === 'undefined') return;
  if (userId) {
    localStorage.setItem(`appTheme_${userId}`, themeName);
  }
  applyAppTheme(themeName);
  const gradient = getStoredTextGradient(userId);
  applyTextGradient(gradient);
};

export const applyAppTheme = (themeName) => {
  const theme = appThemes[themeName];
  if (!theme) return;
  const root = document.documentElement;
  Object.entries(theme.colors).forEach(([key, value]) => {
    root.style.setProperty(key, value);
  });
};

let gradientElements = [];

export const applyTextGradient = (gradientKey) => {
  const gradient = textGradients[gradientKey];
  if (!gradient) return;
  const root = document.documentElement;
  const body = typeof document !== 'undefined' ? document.body : null;

  // Remove gradient from all previously styled elements
  gradientElements.forEach(el => {
    el.style.removeProperty('background');
    el.style.removeProperty('-webkit-background-clip');
    el.style.removeProperty('background-clip');
    el.style.removeProperty('-webkit-text-fill-color');
    el.style.removeProperty('color');
  });
  gradientElements = [];

  root.style.setProperty('--text-gradient', gradient.value || 'none');

  if (gradient.value) {
    if (body) body.classList.add('text-gradient-active');

    const skipTags = new Set(['img','svg','video','iframe','canvas','style','script','input','textarea','select','option','noscript','template']);

    function walk(el) {
      if (el.nodeType !== 1) return;
      const tag = el.tagName.toLowerCase();
      if (skipTags.has(tag)) return;

      let hasDirectText = false;
      const children = el.childNodes;
      for (let i = 0; i < children.length; i++) {
        const child = children[i];
        if (child.nodeType === 3 && child.textContent.trim().length > 0) {
          hasDirectText = true;
          break;
        }
      }

      if (hasDirectText) {
        const fontIcons = ['fa','fas','far','fal','fad','fab','material-icons','material-symbols-outlined','bi'];
        let isIcon = false;
        for (let i = 0; i < fontIcons.length; i++) {
          if (el.classList.contains(fontIcons[i])) { isIcon = true; break; }
        }
        if (isIcon) return;

        const cs = getComputedStyle(el);
        const bgColor = cs.backgroundColor;
        const bgImage = cs.backgroundImage;

        const hasOwnBg = bgImage !== 'none' ||
          (bgColor !== 'rgba(0, 0, 0, 0)' &&
           bgColor !== 'transparent' &&
           bgColor !== '');

        if (!hasOwnBg) {
          el.style.setProperty('background', gradient.value);
          el.style.setProperty('-webkit-background-clip', 'text');
          el.style.setProperty('background-clip', 'text');
          el.style.setProperty('-webkit-text-fill-color', 'transparent');
          el.style.setProperty('color', 'transparent');
          gradientElements.push(el);
        }
      }

      for (let i = 0; i < el.children.length; i++) {
        walk(el.children[i]);
      }
    }

    walk(body);
  } else {
    if (body) body.classList.remove('text-gradient-active');
  }
};

export const persistTextGradient = (userId, gradientKey) => {
  if (typeof window === 'undefined') return;
  if (userId) {
    localStorage.setItem(`textGradient_${userId}`, gradientKey);
  }
  applyTextGradient(gradientKey);
};

